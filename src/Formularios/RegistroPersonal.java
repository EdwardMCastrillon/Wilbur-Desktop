                jCtiporh.setSelectedIndex(buscarPersonal.getRh());
                jCtiposangre.setSelectedIndex(buscarPersonal.getSangre());
                jcmbTipoContrato.setSelectedIndex(buscarPersonal.getTipoContrato());
